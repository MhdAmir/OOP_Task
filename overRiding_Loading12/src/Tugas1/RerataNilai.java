package Tugas1;

public class RerataNilai {

    int average(int nilai1, int nilai2) {
        return nilai1 + nilai2 / 2;
    }

    double average(double nilai1, double nilai2) {
        return nilai1 + nilai2 / 2;
    }

    int average(int nilai1, int nilai2, int nilai3) {
        return nilai1 + nilai2 + nilai3 / 3;
    }
}
